inline double get_sn_factor(size_t n) {
  if (n <= 100)
    return factors_sn[n - 1];
  double inv_n = 1.0 / static_cast<double>(n);
  if (n % 2 == 1)
    return 1.0 + 0.7096 * inv_n - 7.3604 * inv_n * inv_n;
  else
    return 1.0 + 0.0391 * inv_n - 6.1719 * inv_n * inv_n;
}

inline double get_qn_factor(size_t n) {
  if (n <= 100)
    return factors_qn[n - 1];
  double inv_n = 1.0 / static_cast<double>(n);
  if (n % 2 == 1)
    return 1.0 - 1.6022 * inv_n + 4.7453 * inv_n * inv_n;
  else
    return 1.0 - 3.6741 * inv_n + 11.1030 * inv_n * inv_n;
}

